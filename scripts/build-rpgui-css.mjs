/* Build src/rpgui-css.js from the vendored assets/rpgui/rpgui.css.
   - strips the @import (Press Start 2P is loaded via <link> in rpgui.web.js)
   - inlines every url(img/...) as a base64 data URI (robust under Metro + baseUrl)
   - appends Alchemist overrides so .rpgui-content cooperates with the RNW flex root
   Run: node scripts/build-rpgui-css.mjs  (from the alchemist/ project dir) */
import fs from 'fs';
import path from 'path';

const dir = path.resolve('assets/rpgui');
let css = fs.readFileSync(path.join(dir, 'rpgui.css'), 'utf8');

// font is loaded via <link> instead of @import (so it works inside an injected <style>)
css = css.replace(/@import\s+url\([^)]*\)\s*;?/g, '');

const mimeOf = (f) =>
  f.endsWith('.png') ? 'image/png'
  : f.endsWith('.gif') ? 'image/gif'
  : f.endsWith('.cur') ? 'image/x-icon'
  : f.endsWith('.jpg') || f.endsWith('.jpeg') ? 'image/jpeg'
  : 'application/octet-stream';

// widgets/icons the app never renders → replace with a 1x1 transparent gif to shrink the bundle
const SKIP = /(slider|radio|scrollbar|select-|golden2|checkbox)|icons\/(weapon-slot|shield-slot|potion-slot)/;
const BLANK = 'url("data:image/gif;base64,R0lGODlhAQABAAAAACH5BAEKAAEALAAAAAABAAEAAAICTAEAOw==")';

let inlined = 0, skipped = 0, missing = [];
const used = new Set();
css = css.replace(/url\(\s*(['"]?)([^'")]+)\1\s*\)/g, (m, _q, ref) => {
  if (ref.startsWith('data:') || /^https?:/i.test(ref)) return m;
  const clean = ref.split('?')[0].split('#')[0].trim();
  if (!clean) return m;
  if (SKIP.test(clean)) { skipped++; return BLANK; }
  const fp = path.join(dir, clean);
  let st;
  try { st = fs.statSync(fp); } catch (e) { missing.push(clean); return m; }
  if (!st.isFile()) { missing.push(clean + ' (not a file)'); return m; }
  // emit a placeholder; the real Metro-hashed uri is substituted at runtime (keeps art out of the JS bundle)
  used.add(clean);
  inlined++;
  return `url("RPGUIIMG:${clean}")`;
});

const overrides = `
/* ---- Alchemist overrides ---- */
.rpgui-content { position: relative !important; left: auto !important; top: auto !important; right: auto !important; bottom: auto !important; width: 100% !important; height: 100% !important; display: flex !important; flex-direction: column !important; }
/* neutralize RPGUI default margins so RN flex controls spacing */
.rpgui-content .rpgui-container, .rpgui-content .rpgui-button, .rpgui-content .rpgui-progress, .rpgui-content .rpgui-icon { margin: 0 !important; box-sizing: border-box !important; }
.rpgui-content .rpgui-button > p { margin: 0 !important; }
/* RPGUI centers container text by default — leaks into our card content */
.rpgui-content .rpgui-container { text-align: left !important; }
/* RPGUI applies image-rendering: pixelated globally; keep our vector art (radar/timer/charts) crisp */
.rpgui-content svg, .rpgui-content svg * { image-rendering: auto !important; }
/* Cyrillic-capable pixel font (VT323) everywhere, incl. native RPGUI button/input/label text */
.rpgui-content, .rpgui-content p, .rpgui-content input, .rpgui-content textarea, .rpgui-content label, .rpgui-content button { font-family: 'VT323', monospace !important; }
`;
css += overrides;

const out =
  '/* AUTO-GENERATED by scripts/build-rpgui-css.mjs — do not edit by hand */\n' +
  'export const RPGUI_CSS = ' + JSON.stringify(css) + ';\n';

fs.writeFileSync(path.resolve('src/rpgui-css.js'), out);

// web-only require map: clean path -> Metro asset (resolved to a hashed uri at runtime)
const imgMod =
  '/* AUTO-GENERATED by scripts/build-rpgui-css.mjs — do not edit by hand */\n' +
  'export const RPGUI_IMG = {\n' +
  [...used].sort().map((c) => `  ${JSON.stringify(c)}: require(${JSON.stringify('../assets/rpgui/' + c)}),`).join('\n') +
  '\n};\n';
fs.writeFileSync(path.resolve('src/rpgui-img.web.js'), imgMod);
console.log(`wrote src/rpgui-css.js (${out.length} bytes, ${inlined} refs, skipped ${skipped}) + rpgui-img.web.js (${used.size} images)`);
if (missing.length) console.warn('MISSING:', missing.join(', '));
